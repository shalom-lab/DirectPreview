function buildLocale(localeId, translations) {
  const out = {}
  for (const [key, meta] of Object.entries(en)) {
    const message = translations[key] ?? meta.message
    out[key] = { message, description: meta.description }
    if (meta.placeholders) out[key].placeholders = meta.placeholders
  }
  for (const [key, label] of Object.entries(LANG_LABELS)) {
    out[key] = { message: label, description: "Language name" }
  }
  return out
}

